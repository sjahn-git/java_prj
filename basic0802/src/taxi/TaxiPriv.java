package taxi;

public class TaxiPriv {
	private String yongin;
	private String sora;
	private String tt;
	
	private int vv;
	
	private double rr; 
	
	
	String getYongin() {return this.yongin;}
	void setYongin(String yongin) {this.yongin = yongin;}
	
	String getSora() {return this.sora;}
	void setSora(String sora) {this.sora = sora;}
	
	String getTt() {return this.tt;}
	void setTt(String tt) {this.tt = tt;}
	
	int getVv() {return this.vv;}
	void setVv(int vv) {this.vv = vv;}
	
	double getRr() {return this.rr;}
	void setRr(double rr) {this.rr = rr;}
}
